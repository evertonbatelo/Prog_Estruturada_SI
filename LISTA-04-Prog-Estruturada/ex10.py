# tupla inicial
tupla = (1, 2, 3, 4, 5)
print("Tupla original:", tupla)

# converter tupla em lista
lista = list(tupla)
print("Convertida em lista:", lista)

# modificar a lista (exemplo)
lista.append(6)
print("Lista modificada:", lista)

# converter lista de volta para tupla
tupla_nova = tuple(lista)
print("Convertida novamente em tupla:", tupla_nova)